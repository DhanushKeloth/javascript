//arithmetic operators
a=10
b=2
console.log("arithmetic operators")
console.log(a+b)
console.log(a-b)
console.log(a*b)
console.log(a/b)
console.log(a%b)
console.log(a**b)
console.log("\n")

//comparison operators
console.log("comparison operators")
console.log(a==b)
console.log(a!=b)
console.log(a>b)
console.log(a<b)
console.log(a>=b)
console.log(a<=b)
console.log("\n")

//strict equality 
console.log("strict comparison operators")
num1=100
num2="100"
console.log(num1===num2)
console.log(num1!==num2)
console.log("\n")

//logical operators
console.log("logical operators")
console.log(a>b && a<b)
console.log(a<b || a>b)
console.log(!a)

//type coercion 
console.log(10+"20") ///string concatenation
console.log(10-"5") ///number substraction
console.log(10*"5") ///number multiplication
